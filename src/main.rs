// #[]  是干嘛的
// #[] 符号是用来标识一个属性（attribute）的。
// 属性是元数据（metadata）的一种形式，它可以用于影响
// 编译器的行为、定义代码的运行时行为、向编译器传递一些额外信息等。
// 在 Rust 中，许多关键字都是以 # 开头，例如 #[derive] 和 #[allow] 等。


// example
// #[cfg(target_os = "linux")] 表示一个条件编译属性，
// 其中 cfg 是属性名称，target_os 是属性的键，
// 而 "linux" 则是键对应的值。

// 最主要的思想是与编译器的调用和交互
